// program that takes two int and calculates add,sub,mul,div,mod

#include <iostream>
using namespace std;
void main(){
    int num1,num2;
    cout<<"Enter the two numbers for (addition,subtraction,multiplication,division,modulous)"<<endl;
    cin>>num1>>num2;
    cout<<"Addition : "<<num1+num2<<endl;
    cout<<"Subtraction : "<<num1-num2<<endl;
    cout<<"Multiplication : "<<num1*num2<<endl;
    cout<<"Division : "<<num1/num2<<endl;
    cout<<"Modulous : "<<num1%num2<<endl;
    
}